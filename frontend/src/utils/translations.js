export const tData = (category, value, lang = 'en') => {
    if (!value || lang === 'en') return value;
    const langData = panchagData[lang] || panchagData.hi;

    // Check for exact match first (fixes cases like Ritu "Shishir (Winter)")
    if (langData[category]?.[value]) {
        return langData[category][value];
    }

    // Handle compound values like "Chaturdashi (Shukla-Paksha)"
    if (typeof value === 'string' && value.includes('(')) {
        const [main, extra] = value.split('(');
        const mainKey = main.trim();
        const extraKey = extra.replace(')', '').trim();

        const translatedMain = langData[category]?.[mainKey] || mainKey;

        // Try to translate the extra part (usually Paksha or Sign)
        let translatedExtra = extraKey;
        if (extraKey.includes('-')) {
            const [part1, part2] = extraKey.split('-');
            const translatedPart1 = langData['paksha']?.[part1] || langData[category]?.[part1] || part1;
            translatedExtra = `${translatedPart1}-${part2}`;
        } else {
            translatedExtra = langData['paksha']?.[extraKey] || langData['rashi']?.[extraKey] || langData[category]?.[extraKey] || extraKey;
        }

        return `${translatedMain} (${translatedExtra})`;
    }

    return langData[category]?.[value] || value;
};


