/**
 * Translates a given English term to Hindi.
 * If the term is a panchang string like "Pratipada (Shukla-Paksha)",
 * it parses and translates it.
 */
const translateToHi = (text) => {
    if (!text) return text;

    // Direct match
    if (dictionary.enToHi[text]) {
        return dictionary.enToHi[text];
    }

    let translated = text;

    // Paksha handling
    translated = translated.replace('(Shukla-Paksha)', '(शुक्ल पक्ष)');
    translated = translated.replace('(Krishna-Paksha)', '(कृष्ण पक्ष)');

    // Tithi handling
    const tithis = {
        'Pratipada': 'प्रतिपदा',
        'Dwitiya': 'द्वितीया',
        'Tritiya': 'तृतीया',
        'Chaturthi': 'चतुर्थी',
        'Panchami': 'पंचमी',
        'Shashthi': 'षष्ठी',
        'Saptami': 'सप्तमी',
        'Ashtami': 'अष्टमी',
        'Navami': 'नवमी',
        'Dashami': 'दशमी',
        'Ekadashi': 'एकादशी',
        'Dwadashi': 'द्वादशी',
        'Trayodashi': 'त्रयोदशी',
        'Chaturdashi': 'चतुर्दशी',
        'Purnima': 'पूर्णिमा',
        'Amavasya': 'अमावस्या'
    };

    for (const [en, hi] of Object.entries(tithis)) {
        if (translated.includes(en)) {
            translated = translated.replace(en, hi);
        }
    }

    // Pada handling
    if (translated.includes('(Pada ')) {
        translated = translated.replace('(Pada 1)', '(पद १)');
        translated = translated.replace('(Pada 2)', '(पद २)');
        translated = translated.replace('(Pada 3)', '(पद ३)');
        translated = translated.replace('(Pada 4)', '(पद ४)');
    }

    return translated;
};

/**
 * Translates a given English term to Telugu.
 */
const translateToTe = (text) => {
    if (!text) return text;
    if (dictionary.enToTe[text]) return dictionary.enToTe[text];
    let translated = text;
    translated = translated.replace('(Shukla-Paksha)', '(శుక్ల పక్షం)');
    translated = translated.replace('(Krishna-Paksha)', '(కృష్ణ పక్షం)');
    const tithis = {
        'Pratipada': 'పాడ్యమి', 'Dwitiya': 'విదియ', 'Tritiya': 'తదియ', 'Chaturthi': 'చవితి', 'Panchami': 'పంచమి', 'Shashthi': 'షష్ఠి', 'Saptami': 'సప్తమి', 'Ashtami': 'అష్టమి', 'Navami': 'నవమి', 'Dashami': 'దశమి', 'Ekadashi': 'ఏకాదశి', 'Dwadashi': 'ద్వాదశి', 'Trayodashi': 'త్రయోదశి', 'Chaturdashi': 'చతుర్దశి', 'Purnima': 'పౌర్ణమి', 'Amavasya': 'అమావాస్య'
    };
    for (const [en, te] of Object.entries(tithis)) {
        if (translated.includes(en)) translated = translated.replace(en, te);
    }
    if (translated.includes('(Pada ')) {
        translated = translated.replace('(Pada 1)', '(పాదం 1)');
        translated = translated.replace('(Pada 2)', '(పాదం 2)');
        translated = translated.replace('(Pada 3)', '(పాదం 3)');
        translated = translated.replace('(Pada 4)', '(పాదం 4)');
    }
    return translated;
};

